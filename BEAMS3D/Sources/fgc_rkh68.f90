!     Description:   Just a wrapper to fgc_nag.
!
!-----------------------------------------------------------------------
      SUBROUTINE fgc_rkh68(t,q,qdot,istat)
!-----------------------------------------------------------------------
!     Libraries NONE
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
!     Input Variables
!          t          time coordinates
!          q          (q(1),q(2),q(3),q(4)) = (R,phi,Z,vll)
!          qdot       dq/dt
!-----------------------------------------------------------------------
      IMPLICIT NONE
      INTEGER          :: istat
      DOUBLE PRECISION :: t, q(4), qdot(4)
!-----------------------------------------------------------------------
!     Begin Subroutine
!------------------------------------------------
      CALL fgc_eom(t,q,qdot)
      istat = 0
      RETURN
!-----------------------------------------------------------------------
!     End Subroutine
!-----------------------------------------------------------------------
      END SUBROUTINE fgc_rkh68
