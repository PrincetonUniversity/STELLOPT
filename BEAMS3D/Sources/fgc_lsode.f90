!-----------------------------------------------------------------------
      SUBROUTINE fgc_lsode(neq,t,q,qdot)
!-----------------------------------------------------------------------
!     Libraries  NONE
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
!     Input Variables
!          t          time coordinates
!          q          (q(1),q(2),q(3),q(4)) = (R,phi,Z,vll)
!          qdot       dq/dt
!-----------------------------------------------------------------------
      IMPLICIT NONE
      INTEGER          :: neq
      DOUBLE PRECISION :: t, q(4), qdot(4)
!-----------------------------------------------------------------------
!     Begin Subroutine
!------------------------------------------------
      CALL fgc_eom(t,q,qdot)
      RETURN
!-----------------------------------------------------------------------
!     End Subroutine
!-----------------------------------------------------------------------
      END SUBROUTINE fgc_lsode
